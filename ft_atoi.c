int	ft_atoi(char *str)
{
	int res;
	int sign;
	int i;

	i = 0;
	res = 0;
	sign = 1;
	if (str[0] = '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] != '\0')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (sign * res);
}
